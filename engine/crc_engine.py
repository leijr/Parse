# ── CAN ID to Data ID mapping for CRC-16 Profile 5 ───────────────────────
# Defined by the communication matrix. When a CAN frame carries data,
# the CRC is calculated over the data payload identified by this mapped ID.
CANID_DATAID_MAP: dict[str, str] = {
    '198': '322', '32c': '31A', '3b0': '332', '268': '11A',
    '160': '409', '180': '339', '188': '341', '338': '349',
    '3b8': '351', '3a2': '302', '370': '329', '170': '301',
    '360': '309', '179': '311', '171': '306', '362': '307',
    '332': '32d',
}

CAN = 2
CANFD = 1


def crc8_profile1(can_id: str, data_hex: str) -> str:
    """CRC-8 Profile 1 calculation.

    Args:
        can_id: CAN frame ID as hex string (e.g. '170')
        data_hex: 7 data bytes as hex string (e.g. '01020304050607')

    Returns:
        2-char hex string CRC value
    """
    id_int = int(can_id, 16)
    table = CRC8_TABLE
    crc = 0xFF
    crc ^= id_int & 0x00ff
    crc = table[crc]
    crc ^= (id_int >> 8) & 0x00ff
    crc = table[crc]

    for i in range(7):
        byte_val = int(data_hex[2 * i:2 * i + 2], 16)
        crc ^= byte_val
        crc = table[crc]

    crc ^= 0xFF
    result = f"{crc:02x}"
    return result


def crc16_profile5(can_id: str, data_hex: str) -> str:
    """CRC-16 Profile 5 calculation.

    Args:
        can_id: CAN frame ID as hex string
        data_hex: 64-byte CANFD payload as hex string (128 chars)

    Returns:
        4-char hex string CRC value (byte-swapped)
    """
    table = CRC16_TABLE
    crc = 0xFFFF
    for i in range(2, 56):
        byte_val = int(data_hex[2 * i:2 * i + 2], 16)
        crc ^= (byte_val << 8)
        crc = ((crc << 8) & 0xFFFF) ^ table[(crc >> 8) & 0xFF]

    data_id = CANID_DATAID_MAP.get(can_id, can_id)
    data_id_int = int(data_id, 16)
    crc ^= data_id_int << 8
    crc = ((crc << 8) & 0xFFFF) ^ table[(crc >> 8) & 0xff]
    crc ^= data_id_int & 0xFF00
    crc = ((crc << 8) & 0xFFFF) ^ table[(crc >> 8) & 0xff]
    crc ^= 0x0000

    result = f"{crc:04x}"
    result = result[-2:] + result[:2]  # byte swap
    return result


class CRCValidator:
    """Validate CRC checksums in ASC log data."""

    MAX_SAMPLES = 100

    def __init__(self, can_ids: list[str], asc_filepath: str, can_type: int):
        self.can_ids = can_ids
        self.asc_filepath = asc_filepath
        self.can_type = can_type
        self.data: dict[str, dict] = {}
        self.results: list[str] = []

    def run(self) -> list[str]:
        self._init_data()
        self._parse_asc()
        self._verify()
        self._save_csv()
        return self.results

    def _init_data(self):
        for cid in self.can_ids:
            self.data[cid] = {"contents": [], "check_result": [], "result_str": []}

    def _parse_asc(self):
        with open(self.asc_filepath, "r") as f:
            for line in f:
                stripped = line.replace(" ", "")
                if self.can_type == CANFD:
                    for i in range(len(stripped)):
                        if stripped[i:i + 2] == "Rx":
                            cid = stripped[i + 2:i + 5].lower()
                            if cid in self.data and len(self.data[cid]["contents"]) < self.MAX_SAMPLES:
                                for j in range(len(stripped)):
                                    if stripped[j:j + 3] == "f64" and j - i < 25:
                                        self.data[cid]["contents"].append(stripped[j + 3:j + 3 + 128])
                                        break
                else:  # CAN
                    for i in range(len(stripped)):
                        if stripped[i:i + 2] == "Rx":
                            cid = stripped[i - 3:i].lower()
                            if cid in self.data and len(self.data[cid]["contents"]) < self.MAX_SAMPLES:
                                self.data[cid]["contents"].append(stripped[i + 4:i + 4 + 16])
                                break

    def _verify(self):
        self.results = []
        for cid, info in self.data.items():
            contents = info["contents"]
            if not contents:
                self.results.append(f"{cid}: No data found in ASC file")
                continue

            for payload in contents:
                if self.can_type == CANFD:
                    calc = crc16_profile5(cid, payload)
                    match = calc == payload[:4].lower()
                else:
                    calc = crc8_profile1(cid, payload)
                    match = calc == payload[-2:].lower()

                info["check_result"].append(calc)
                info["result_str"].append(1 if match else 0)

            if 0 in info["result_str"]:
                self.results.append(f"{cid}: CHECK FAILED")
            else:
                self.results.append(f"{cid}: All checks passed")

    def _save_csv(self):
        lengths = [len(v["contents"]) for v in self.data.values()]
        if not lengths or max(lengths) == 0:
            return
        max_len = max(lengths)

        for info in self.data.values():
            deficit = max_len - len(info["contents"])
            if deficit > 0:
                info["contents"].extend([""] * deficit)
                info["check_result"].extend([""] * deficit)
                info["result_str"].extend([""] * deficit)

        csv_data = {}
        for cid, info in self.data.items():
            csv_data[f"{cid}_Data"] = info["contents"]
            csv_data[f"{cid}_Calculation"] = info["check_result"]
            csv_data[f"{cid}_Check_Result"] = info["result_str"]

        df = pd.DataFrame(csv_data)
        basename = os.path.basename(self.asc_filepath)[:-4]
        ts = datetime.datetime.now().strftime("%Y_%m_%d_%H_%M")
        path = os.path.join(os.getcwd(), f"{basename}_CRC_{ts}.csv")
        df.to_csv(path, index=False)
